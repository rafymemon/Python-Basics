# Methods for Number Conversions

print(bin(4))  # Numbers to Binary value
print(0b100)  # Binary value to Numbers
print(int("100", 2))  # Using int() method to convert binary number


print(oct(144))  # Natural Numbers to Octal Numbers
print(0o220)  # Octal Value to Natural Numbers
print(int("220", 8))  # Using int() method to convert octal number

print(hex(255))  # Numbers to Hexa-decimal values
print(0xFF)  # Hexa-decimal values to numbers
print(int("255", 16))  # Using int() method to convert hexa-decimal number
